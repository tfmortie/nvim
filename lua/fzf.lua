--
-- FZF SETTINGS
--

-- set mapping for files
vim.keymap.set("n", "<space>f", ":Files<cr>")
-- set mapping for buffers
vim.keymap.set("n", "<space>b", ":Buffers<cr>")
-- set mapping for lines
vim.keymap.set("n", "<space>m", ":Lines<cr>")
-- set mapping for (current) buffer lines
vim.keymap.set("n", "<space>l", ":BLines<cr>")
